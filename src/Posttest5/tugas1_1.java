    private void tblpelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblpelangganMouseClicked
        DefaultTableModel model = (DefaultTableModel) tblpelanggan.getModel();
        nama.setText(model.getValueAt(tblpelanggan.getSelectedRow(), 0).toString());
        telepon.setText(model.getValueAt(tblpelanggan.getSelectedRow(), 1).toString());
        kota.setSelectedItem(model.getValueAt(tblpelanggan.getSelectedRow(), 2).toString());
        if(laki.isSelected()) {
            laki.setText(model.getValueAt(tblpelanggan.getSelectedRow(), 3).toString());
        }
        else {
            perempuan.setText(model.getValueAt(tblpelanggan.getSelectedRow(), 3).toString());
        }
        if(ckaktif.isSelected()) {
            ckaktif.setText(model.getValueAt(tblpelanggan.getSelectedRow(), 4).toString());
        }
        else {
            ckaktif.setText(model.getValueAt(tblpelanggan.getSelectedRow(), 4).toString());
        }
        nama.setText(model.getValueAt(tblpelanggan.getSelectedRow(), 5).toString());
    }//GEN-LAST:event_tblpelangganMouseClicked

