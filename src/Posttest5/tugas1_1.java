    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        try {
            //            if(!txtnama.getText().trim().equals("") && !txttelepon.getText().trim().equals("") && !txtalamat.getText().trim().equals("")) {
                DefaultTableModel model = (DefaultTableModel) tblpelanggan.getModel();
                model.setValueAt(nama.getText(), tblpelanggan.getSelectedRow(), 0);
                model.setValueAt(telepon.getText(), tblpelanggan.getSelectedRow(), 1);
                model.setValueAt(kota.getSelectedItem().toString(), tblpelanggan.getSelectedRow(), 2);
                if(laki.isSelected()) {
                    model.setValueAt(laki.getText(), tblpelanggan.getSelectedRow(), 3);
                }
                else if(perempuan.isSelected()) {
                    model.setValueAt(perempuan.getText(), tblpelanggan.getSelectedRow(), 3);
                }
                if(ckaktif.isSelected()) {
                    model.setValueAt(ckaktif.getText(), tblpelanggan.getSelectedRow(), 4);
                }
                else if(ckaktif.isSelected()) {
                    model.setValueAt(ckaktif.getText(), tblpelanggan.getSelectedRow(), 4);
                }
                model.setValueAt(alamat.getText(), tblpelanggan.getSelectedRow(), 5);
                txtdanger.setText("Record Berhasil di Update");
        }
        catch (ArrayIndexOutOfBoundsException e) {
            txtdanger.setText("Pastikan ada record terpilih dan lengkapi form isian");
        }
        btntambah.setEnabled(true);
        btnsimpan.setEnabled(false);
        btnupdate.setEnabled(false);
        btnhapus.setEnabled(false);
        nama.setEnabled(false);
        telepon.setEnabled(false);
        kota.setEnabled(false);
        laki.setEnabled(false);
        perempuan.setEnabled(false);
        alamat.setEnabled(false);
        laki.setSelected(true);
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed

        if(!nama.getText().trim().equals("") && !telepon.getText().trim().equals("") && !alamat.getText().trim().equals("")) {
            Object[] data = new Object[6];
            data[0] = nama.getText();
            data[1] = telepon.getText();
            data[2] = kota.getSelectedItem().toString();
            if (laki.isSelected()) {
                data[3] = laki.getText();
            }
            else if (perempuan.isSelected()) {
                data[3] = perempuan.getText();
            }
            if (ckaktif.isSelected()) {
                data[4] = ckaktif.getText();
            }
            else if (ckaktif.isSelected()) {
                data[4] = ckaktif.getText();
            }
            data[5] = alamat.getText();

            model1.addRow(data);
            btntambah.setEnabled(true);
            btnsimpan.setEnabled(false);
            btnupdate.setEnabled(false);
            btnhapus.setEnabled(false);
            nama.setEnabled(false);
            telepon.setEnabled(false);
            kota.setEnabled(false);
            laki.setEnabled(false);
            perempuan.setEnabled(false);
            alamat.setEnabled(false);
            laki.setSelected(true);
        }
        else {
            txtdanger.setText("*isian tidak boleh ada yang kosong");
            btntambah.setEnabled(true);
            btnsimpan.setEnabled(false);
            btnupdate.setEnabled(false);
            btnhapus.setEnabled(false);
            nama.setEnabled(false);
            telepon.setEnabled(false);
            kota.setEnabled(false);
            laki.setEnabled(false);
            perempuan.setEnabled(false);
            alamat.setEnabled(false);
            laki.setSelected(true);
        }

    }//GEN-LAST:event_btnsimpanActionPerformed

