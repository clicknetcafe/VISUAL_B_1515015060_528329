    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed

    if(!txtnama.getText().trim().equals("") && !txttelepon.getText().trim().equals("") && !txtalamat.getText().trim().equals("")) {
        Object[] data = new Object[6];
        data[0] = txtnama.getText();
        data[1] = txttelepon.getText();
        data[2] = txtkota.getSelectedItem().toString();
        if (btnlaki.isSelected()) {
            data[3] = btnlaki.getText();
        }
        else if (btncewe.isSelected()) {
            data[3] = btncewe.getText();
        }
        data[4] = sltinggi.getValue();
        data[5] = txtalamat.getText();
               
        model.addRow(data);
        btntambah.setEnabled(true);
        btnsimpan.setEnabled(false);
        btnupdate.setEnabled(false);
        btnhapus.setEnabled(false);
        txtnama.setEnabled(false);
        txttelepon.setEnabled(false);
        txtkota.setEnabled(false);
        btnlaki.setEnabled(false);
        btncewe.setEnabled(false);
        sltinggi.setEnabled(false);
        jLabel8.setEnabled(false);
        txtalamat.setEnabled(false);
        txttinggi.setEnabled(false);
        btnlaki.setSelected(true);
    }
    else {
	txtdanger.setText("*isian tidak boleh ada yang kosong");
        btntambah.setEnabled(true);
        btnsimpan.setEnabled(false);
        btnupdate.setEnabled(false);
        btnhapus.setEnabled(false);
        txtnama.setEnabled(false);
        txttelepon.setEnabled(false);
        txtkota.setEnabled(false);
        btnlaki.setEnabled(false);
        btncewe.setEnabled(false);
        sltinggi.setEnabled(false);
        jLabel8.setEnabled(false);
        txtalamat.setEnabled(false);
        txttinggi.setEnabled(false);
        btnlaki.setSelected(true);
    }
        
    }//GEN-LAST:event_btnsimpanActionPerformed

