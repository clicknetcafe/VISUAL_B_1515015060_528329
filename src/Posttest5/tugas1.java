    private void tblpelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblpelangganMouseClicked
        DefaultTableModel model = (DefaultTableModel) tblpelanggan.getModel();
        txtnama.setText(model.getValueAt(tblpelanggan.getSelectedRow(), 0).toString());
        txttelepon.setText(model.getValueAt(tblpelanggan.getSelectedRow(), 1).toString());
        txtkota.setSelectedItem(model.getValueAt(tblpelanggan.getSelectedRow(), 2).toString());
        if(btnlaki.isSelected()) {
            btnlaki.setText(model.getValueAt(tblpelanggan.getSelectedRow(), 3).toString());
        }
        else {
            btncewe.setText(model.getValueAt(tblpelanggan.getSelectedRow(), 3).toString());
        }
        sltinggi.setValue((int) model.getValueAt(tblpelanggan.getSelectedRow(), 4));
        txtnama.setText(model.getValueAt(tblpelanggan.getSelectedRow(), 5).toString());
    }//GEN-LAST:event_tblpelangganMouseClicked

